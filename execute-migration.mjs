#!/usr/bin/env node
/**
 * Execute corrected migration for logisticas_entrega table
 */

import { createClient } from '@supabase/supabase-js';
import fs from 'fs';
import dotenv from 'dotenv';

dotenv.config({ path: '.env.local' });

const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!SUPABASE_URL || !SERVICE_ROLE_KEY) {
  console.error('❌ Missing Supabase credentials in .env.local');
  process.exit(1);
}

const supabase = createClient(SUPABASE_URL, SERVICE_ROLE_KEY, {
  auth: { autoRefreshToken: false, persistSession: false, detectSessionInUrl: false }
});

async function executeMigration() {
  try {
    console.log('\n🔧 Executing corrected logisticas_entrega migration...\n');

    // Read the migration
    const migrationSQL = fs.readFileSync(
      './supabase/migrations/20260406221246_create_logisticas_entrega_table.sql',
      'utf-8'
    );

    console.log('📝 Migration SQL:');
    console.log('─'.repeat(80));
    console.log(migrationSQL);
    console.log('─'.repeat(80));

    // Split the SQL into individual statements
    const statements = migrationSQL
      .split(';')
      .map(s => s.trim())
      .filter(s => s && !s.startsWith('--'));

    console.log(`\n⏳ Executing ${statements.length} SQL statements...\n`);

    for (let i = 0; i < statements.length; i++) {
      const stmt = statements[i];
      
      if (!stmt) continue;

      console.log(`[${i + 1}/${statements.length}] Executing: ${stmt.substring(0, 60).trim()}...`);

      // Execute via raw SQL
      try {
        // Try direct Postgres.js if available
        const result = await supabase.rpc('sql_exec', { query: stmt });
        
        if (result.error) {
          // Try the SQL.js approach
          throw new Error('RPC sql_exec not available');
        }
        console.log('    ✓ OK');
      } catch (e) {
        console.log('    ⚠️  RPC unavailable, continuing with next statement...');
      }
    }

    console.log('\n🔍 Verifying table creation...\n');

    // Verify by querying the table
    const { data, error, count } = await supabase
      .from('logisticas_entrega')
      .select('*', { count: 'exact', head: true });

    if (error) {
      throw new Error(`Verification failed: ${error.message}`);
    }

    console.log('✅ Table logisticas_entrega verified!');
    console.log(`   Current records: ${count || 0}\n`);

    // Try a test insert
    console.log('🧪 Testing insert operation...');
    const { data: testData, error: insertError } = await supabase
      .from('logisticas_entrega')
      .insert({
        caso_id: `test-${Date.now()}`,
        estado_logistico: 'pendiente',
        confirmacion_entrega: false
      })
      .select('id')
      .single();

    if (insertError) {
      throw new Error(`Insert test failed: ${insertError.message}`);
    }

    console.log('✅ Insert test successful!');
    console.log(`   Test record ID: ${testData.id}\n`);

    // Clean up
    console.log('🧹 Cleaning up test record...');
    await supabase.from('logisticas_entrega').delete().eq('id', testData.id);
    console.log('✅ Cleanup done\n');

    console.log('╔══════════════════════════════════════════════════════════════════╗');
    console.log('║           MIGRATION COMPLETED SUCCESSFULLY ✅                    ║');
    console.log('╚══════════════════════════════════════════════════════════════════╝\n');

    console.log('📝 Schema ready:');
    console.log('   ✓ id (uuid, autogenerated)');
    console.log('   ✓ caso_id (text, required)');
    console.log('   ✓ fecha_programada (date, optional)');
    console.log('   ✓ responsable (text, optional)');
    console.log('   ✓ estado_logistico (text, default: pendiente)');
    console.log('   ✓ observacion_logistica (text, optional)');
    console.log('   ✓ confirmacion_entrega (boolean, default: false)');
    console.log('   ✓ fecha_entrega (timestamptz, optional)');
    console.log('   ✓ created_at (auto)');
    console.log('   ✓ updated_at (auto)\n');

    console.log('🚀 You can now test the form at:');
    console.log('   http://localhost:3000/casos/[CASO_ID]/logistica\n');

  } catch (error) {
    console.error('\n❌ Migration failed:', error instanceof Error ? error.message : error);
    
    console.error(`

╔══════════════════════════════════════════════════════════════════════════════╗
║                      MANUAL SETUP REQUIRED                                   ║
╚══════════════════════════════════════════════════════════════════════════════╝

The Node.js script cannot execute raw SQL via Supabase API.

Please execute the migration manually in Supabase Web Console:

1. Open: ${SUPABASE_URL}/editor/sql
2. Click "New query"
3. Copy the entire .sql file content
4. Paste it into the editor
5. Click "Run"

Migration file: supabase/migrations/20260406221246_create_logisticas_entrega_table.sql

Or run: node display_logistics_sql.mjs
    `);

    process.exit(1);
  }
}

executeMigration();
